countries = ['Japan','UAE','South Korea','Canada','USA']
print("ORIGINAL ORDER")
print(countries)
print("\n")

print("SORT WITHOUT MODIFYING")
print(sorted(countries))
print("\n")

print("ORIGINAL ORDER")
print(countries)
print("\n")

print("PRINT IN REVERSE ORDER")
print(sorted(countries, reverse=True))
print("\n")

print("ORIGINAL ORDER")
print(countries)
print("\n")

print("REVERSE ORDER")
countries.reverse()
print(countries)
print("\n")

print("REVERSE BACK")
countries.reverse()
print(countries)
print("\n")

print("SORT")
countries.sort()
print(countries)
print("\n")

print("SORT REVERSE")
countries.sort(reverse=True)
print(countries)
print("\n")

