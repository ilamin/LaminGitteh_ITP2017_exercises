print("STAGES OF LIFE")
baby = int(input("Enter number : "))
if baby < 2:
    print("You are a baby")
if baby >=2 and baby<4:
    print("You are a toddler")
if baby >=4 and baby<13:
    print("You are a kid")
if baby >=13 and baby<20:
    print("You are a teenager")
if baby >=20 and baby<65:
    print("You are a adult")
if baby >=65 and baby>65:
    print("You are a adult")